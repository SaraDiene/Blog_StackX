// Função para exibir os cards de postagens na página inicial
function displayPosts(categoryFilter = "") {
    const postList = document.getElementById("post-list");
    postList.innerHTML = "";

    for (const post of posts) {
        if (categoryFilter === "" || post.category === categoryFilter) {
            const card = document.createElement("div");
            card.className = "post-card";
            card.innerHTML = `
                <p class="date_card">${post.date}</p>
                <p class="category_card">Categoria: ${post.category}</p>
                <p>${getPostViews(post.id)} visualizações</p>
                <h2 class="title_card">${post.title}</h2>
                <p class="content_card">${post.content.substring(0, 120)}...</p>
            `;
            
            const image = document.createElement("img");
            image.src = `post${post.id}.jpeg`;
            card.appendChild(image);
            
            card.addEventListener("click", () => {
                viewPost(post);
            });
            postList.appendChild(card);
        }
    }
}

// Função para obter o número de visualizações de um post do localStorage
function getPostViews(postId) {
    const views = localStorage.getItem(`postViews_${postId}`);
    return views ? parseInt(views) : 0;
}

// Função para salvar o número de visualizações de um post no localStorage
function savePostViews(postId, views) {
    localStorage.setItem(`postViews_${postId}`, views.toString());
}

// Função para exibir a página de visualização do post
function viewPost(post) {
    const postList = document.getElementById("post-list");
    postList.innerHTML = "";

    const postId = post.id;
    const postViews = getPostViews(postId);

    const postView = document.createElement("div");
    postView.className = "post-view";
    postView.innerHTML = `
        <p>Data: ${post.date}</p>
        <p>Categoria: ${post.category}</p>
        <p>Visualizações: ${postViews + 1}</p>
        <h1>${post.title}</h1>
        <p>${post.content}</p>
    `;

    // Incrementa o número de visualizações e salva no localStorage
    savePostViews(postId, postViews + 1);

    postList.appendChild(postView);
}

